void set_hint_color(int hint_color) {
	if (hint_color == 2) {
		// set green
		printf("\033[0;32m");
	}
	else if (hint_color == 1) {
		// set yellow
		printf("\033[0;33m");
	}
	else {
		// set default
		printf("\033[0m");
	}
}

void print_on_screen(char attempts1[], char answers[], int attempts[], int N) {
	for (int i = 0; i < N - 2; i++) {
		if (attempts1[i] != answers[i]) {
			for (int j = 0; j < N - 2; j++) {
				if (attempts1[i] == answers[j]) {
					attempts[i] = 1;
				}
			}
		}
		else if (attempts1[i] == answers[i]) {
			attempts[i] = 2;
		}
	}
	for (int i = 0; i < N - 2; i++) {
		set_hint_color(attempts[i]);

		printf("+---+");
	}
	printf("\n");
	for (int i = 0; i < N - 2; i++) {
		set_hint_color(attempts[i]);

		printf("| %c |", attempts1[i]);
	}
	printf("\n");
	for (int i = 0; i < N - 2; i++) {
		set_hint_color(attempts[i]);

		printf("+---+");
	}

	printf("\033[0m");
	printf("\n");
}